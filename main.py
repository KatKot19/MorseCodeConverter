def text_to_morse(text):
    morse_text = ''
    for char in text:
        if char.upper() in char_to_morse_dict:
            morse_text += char_to_morse_dict[char]

    return print(morse_text)

def morse_to_text(morse_text):
    text=''
    for morse_char in morse_text.split(' '):
        if morse_char in morse_to_char_dict:
            text+=' '+morse_to_char_dict[morse_char]

    return print(text)
